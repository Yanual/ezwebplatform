  class SetDefaultsCommand(Command):
    option_list = [make_option("--database-engine", action="store",
                               dest="database_engine", help=_("default database engine to use")),
                   make_option("--server-type", action="store",
                               dest="server_type", help=_("default server to use")),
                   make_option("--connection-type", action="store",
                               dest="connection_type", help=_("default connection type to use")),
                   make_option("--theme", action="store",
                               dest="theme", help=_("default theme to use")),
                   make_option("--proxy", action="store",
                               dest="proxy", help=_("default proxy to use")),
                   make_option("-a", "--allow-anonymous-access", action="store",
                               dest="allow_anonymous_access", help=_("Allow/Disallows anonymous accesses by default")),
                   make_option("-d", "--debug-mode", action="store",
                               dest="debug", help=_("Enables/Disables the debug mode")),
                  ]
    args_help = "[schema]"
    args = [Argument("schema", _("Name of the schema to configure. (default: \"default\")"))]
    final = True

    def __init__(self, resources, admintool):
      self.resources = resources
      self.admintool = admintool

    def execute(self, parser, options, args):
      if len(args) > 1:
        return -1
      elif len(args) == 0:
        schema_name = "default"
      else:
        schema_name = args[0]

      self.resources.printlnMsg("Updating \"%s\" schema..." % schema_name)
      self.resources.incPrintNestingLevel()

      # Retreive the list of the instances using this schema
      affectedInstances = self.admintool.get_filtered_instances(["schema", schema_name])


      # Retreive current schema config
      schema_settings = self.admintool.get_default_settings()
      if schema_settings.has_key(schema_name):
        cfg = schema_settings[schema_name]
      else:
        schema_settings[schema_name] = {}
        cfg = schema_settings[schema_name]

      # Database engine
      if options.database_engine != None:
        cfg["database_engine"] = options.database_engine

      # Server type
      if options.server_type != None:
        cfg["server_type"] = options.server_type

      # Connection type
      if options.connection_type != None:
        cfg["connection_type"] = options.connection_type

      # Theme
      if options.theme != None:
        cfg["theme"] = options.theme

      # Proxy
      if options.proxy != None:
        cfg["proxy"] = options.proxy

      # Allow Anonymous Accesses
      if options.allow_anonymous_access != None:
        cfg["allow_anonymous_access"] = options.allow_anonymous_access

      # Debug mode
      if options.debug != None:
        cfg["debug"] = options.debug

      self.admintool.save_default_settings(schema_settings)

      self.resources.printlnMsg("")
      # Update all affected instances
      for instance in affectedInstances:
        self.resources.printlnMsg("Updating \"%s\" instance" % instance.get('name'))
        self.resources.incPrintNestingLevel()

        newconf = self.resources.get_site_config(instance.get('name'), False, False)
        newconf = ConfigCopy(newconf)
        # fill it
        self.admintool.fillConfig(newconf)

        self.admintool._update_instance(instance, newconf, options, True)

        self.resources.decPrintNestingLevel()
        self.resources.printlnMsg("Done")
        self.resources.printlnMsg("")

      self.resources.decPrintNestingLevel()
      self.resources.printlnMsg("Done")
      self.resources.printlnMsg()

